/**
 * The GrillOption enum represents different levels of doneness for grilled items.
 * It is primarily used for burgers, with an additional option of "NONE" for items 
 * that do not require a doneness level, such as chicken or tuna.
 * 
 * @author yosra
 */
public enum GrillOption {
	RARE, MEDIUM, MEDIUM_WELL, WELL_DONE, NONE
}
